package org.Day19Userdefinedvk;

public class Sample {
	
	private int id;
	private String name;
	
	//Encapsulation
	//getters
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	//setters
	public void setId(int id) {
		this.id = id;
	}
	public void setName(String name) 
	{
		this.name = name;
	}

	
}
